#ifndef CHIFFRAGE_H
#define CHIFFRAGE_H

/**
 * @brief Encrypts data using a custom encryption algorithm.
 *
 * This function takes two strings and encrypts the data in the 'in' string
 * using the wishner encryption algorithm, with the result stored in the 'ma' string.
 *
 * @param in The input data to be encrypted.
 * @param ma The output string where the encrypted data will be stored.
 *
 * @return None.
 */
void chiffrage(char *in, char *ma);

#endif /* CHIFFRAGE_H */
